%% GARE POLICY ITERATION WITH DISTURBANCE-AWARE THESIS EXTENSION
%
% This script retains the model-based GARE policy-iteration core and
% appends the disturbance-aware command-channel offset extension.
%
% Nominal system:
%
%   x(k+1) = Ad*x(k) + B*u_P(k) + E*u_E(k)
%
% Nominal saddle policies:
%
%   u_P(k) = L*x(k)     minimizing player / pursuer
%   u_E(k) = K*x(k)     maximizing player / evader
%
% Realized biased inputs:
%
%   u_P_actual(k) = u_P_command(k) + b_P
%   u_E_actual(k) = u_E_command(k) + b_E
%
% Affine compensation:
%
%   u_P_command(k) = L*x(k) - b_P_hat
%   u_E_command(k) = K*x(k) - b_E_hat
%
% Main publication figures:
%
%   1. Convergence of Model-Based GARE Policy Iteration
%   2. Cost-Weighted State-Deviation Recovery
%   3. Late-Time Command-Channel Bias Compensation
%   4. Monte Carlo Validation of Command-Offset Estimates
%
% The full numerical workspace, CSV summaries, and text report are
% saved automatically.
%
% No local functions or additional MATLAB files are required.

