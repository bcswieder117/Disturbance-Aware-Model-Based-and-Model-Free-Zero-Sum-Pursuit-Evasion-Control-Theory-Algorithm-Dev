%% ============================================================
%  FOUR MATCHED CLOSED-LOOP CASES
%  ============================================================

Nsim = 500;

number_of_cases = 4;

case_names = { ...
    'Nominal', ...
    'Without Compensation', ...
    'Estimated Compensation', ...
    'Exact Compensation'};

x_initial = ...
    [10;
      5;
     -2];

%% Plant Offsets

plant_bias_P = ...
    [0, ...
     bP_true, ...
     bP_true, ...
     bP_true];

plant_bias_E = ...
    [0, ...
     bE_true, ...
     bE_true, ...
     bE_true];

%% Command Compensation

command_compensation_P = ...
    [0, ...
     0, ...
     bP_hat, ...
     bP_true];

command_compensation_E = ...
    [0, ...
     0, ...
     bE_hat, ...
     bE_true];

%% Residual Command Offsets

residual_bias_P = ...
    plant_bias_P-command_compensation_P;

residual_bias_E = ...
    plant_bias_E-command_compensation_E;

%% Affine Closed-Loop Forcing

affine_forcing = ...
    B*residual_bias_P ...
    + E*residual_bias_E;

%% Allocate Closed-Loop Arrays

x_case = ...
    zeros(nx,Nsim+1,number_of_cases);

uP_command = ...
    zeros(nuP,Nsim,number_of_cases);

uE_command = ...
    zeros(nuE,Nsim,number_of_cases);

uP_actual = ...
    zeros(nuP,Nsim,number_of_cases);

uE_actual = ...
    zeros(nuE,Nsim,number_of_cases);

state_cost = ...
    zeros(Nsim,number_of_cases);

pursuer_cost = ...
    zeros(Nsim,number_of_cases);

evader_cost = ...
    zeros(Nsim,number_of_cases);

game_stage_cost = ...
    zeros(Nsim,number_of_cases);

%% Simulate the Four Cases

for case_index = 1:number_of_cases

    x_case(:,1,case_index) = x_initial;

    for time_index = 1:Nsim

        %% Policy Commands

        uP_command(:,time_index,case_index) = ...
            L*x_case(:,time_index,case_index) ...
            - command_compensation_P(case_index);

        uE_command(:,time_index,case_index) = ...
            K*x_case(:,time_index,case_index) ...
            - command_compensation_E(case_index);

        %% Realized Inputs

        uP_actual(:,time_index,case_index) = ...
            uP_command(:,time_index,case_index) ...
            + plant_bias_P(case_index);

        uE_actual(:,time_index,case_index) = ...
            uE_command(:,time_index,case_index) ...
            + plant_bias_E(case_index);

        %% Plant Propagation

        x_case(:,time_index+1,case_index) = ...
            Ad*x_case(:,time_index,case_index) ...
            + B*uP_actual(:,time_index,case_index) ...
            + E*uE_actual(:,time_index,case_index);

        %% Cost Components

        state_cost(time_index,case_index) = ...
            x_case(:,time_index,case_index)' ...
            *Q ...
            *x_case(:,time_index,case_index);

        pursuer_cost(time_index,case_index) = ...
            uP_actual(:,time_index,case_index)' ...
            *RP ...
            *uP_actual(:,time_index,case_index);

        evader_cost(time_index,case_index) = ...
            uE_actual(:,time_index,case_index)' ...
            *RE ...
            *uE_actual(:,time_index,case_index);

        game_stage_cost(time_index,case_index) = ...
            state_cost(time_index,case_index) ...
            + pursuer_cost(time_index,case_index) ...
            - evader_cost(time_index,case_index);

    end

end

%% Finite-Window Cost Components

state_cost_total = ...
    sum(state_cost,1)';

pursuer_cost_total = ...
    sum(pursuer_cost,1)';

evader_cost_total = ...
    sum(evader_cost,1)';

game_cost_total = ...
    sum(game_stage_cost,1)';

signed_cost_change = ...
    game_cost_total-game_cost_total(1);

absolute_cost_recovery_error = ...
    abs(signed_cost_change);

%% ============================================================
%  COST-WEIGHTED STATE DEVIATION FROM NOMINAL
%  ============================================================
%
% The plotted metric is:
%
%   ||d_k||_Q = sqrt(d_k'*Q*d_k),
%
% where:
%
%   d_k = x_case(k)-x_nominal(k).
%
% Because Q = I_3, this equals the Euclidean norm for the current
% benchmark while remaining consistent with the state metric used
% in the game objective.

state_deviation = ...
    zeros(nx,Nsim+1,number_of_cases);

weighted_state_deviation = ...
    zeros(Nsim+1,number_of_cases);

maximum_weighted_state_deviation = ...
    zeros(number_of_cases,1);

for case_index = 1:number_of_cases

    state_deviation(:,:,case_index) = ...
        x_case(:,:,case_index)-x_case(:,:,1);

    for time_index = 1:Nsim+1

        deviation_vector = ...
            state_deviation(:,time_index,case_index);

        weighted_state_deviation(time_index,case_index) = ...
            sqrt(max(real( ...
            deviation_vector'*Q*deviation_vector),0));

    end

    maximum_weighted_state_deviation(case_index) = ...
        max(weighted_state_deviation(:,case_index));

end

estimated_error_reduction_percent = ...
    100*(1 ...
    - maximum_weighted_state_deviation(3)/...
