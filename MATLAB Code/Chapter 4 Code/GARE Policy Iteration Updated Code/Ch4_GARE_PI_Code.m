%% ============================================================
%  ORIGINAL DISCRETE-TIME ZERO-SUM BENCHMARK
%  ============================================================

Ad = [0.9064880,  0.0816012, -0.000500000;
      0.0741349,  0.9012100, -0.000708383;
      0,          0,          0.132655000];

B = [-0.00150808;
     -0.00960000;
      0.86734500];

E = [0.00951892;
     0.000383730;
     0];

C = [1 0 -1];

nx = size(Ad,1);
nuP = size(B,2);
nuE = size(E,2);

if nuP ~= 1 || nuE ~= 1

    error(['This benchmark expects one scalar pursuer input ', ...
           'and one scalar evader input.']);

end

%% Original Zero-Sum Cost Parameters

Q = eye(3);

RP = 1;

gamma = 1;

RE = gamma^2;

%% Policy-Iteration Settings

policy_tolerance = 1e-7;

maximum_iterations = 2000;

conditioning_tolerance = 1e-12;

%% Original Initialization

P = eye(nx);

L = zeros(nuP,nx);

K = zeros(nuE,nx);

iteration = 0;

Acl_initial = Ad+B*L+E*K;

initial_spectral_radius = max(abs(eig(Acl_initial)));

if initial_spectral_radius >= 1

    error(['The initial policy pair is not admissible. ', ...
           'rho(Acl_initial) = %.12g.'], ...
           initial_spectral_radius);

end

fprintf('Initial closed-loop spectral radius: %.12g\n', ...
    initial_spectral_radius);

%% Allocate Iteration Histories

P_error_history = nan(maximum_iterations,1);

L_error_history = nan(maximum_iterations,1);

K_error_history = nan(maximum_iterations,1);

spectral_radius_history = nan(maximum_iterations,1);

game_rcond_history = nan(maximum_iterations,1);

converged = false;

%% ============================================================
%  ORIGINAL MODEL-BASED POLICY ITERATION
%  ============================================================

while iteration < maximum_iterations

    iteration = iteration+1;

    P_previous = P;

    L_previous = L;

    K_previous = K;

    %% Policy Evaluation

    A_iteration = ...
        Ad+B*L_previous+E*K_previous;

    current_spectral_radius = ...
        max(abs(eig(A_iteration)));

    if current_spectral_radius >= 1

        error(['Policy evaluation failed at iteration %d. ', ...
               'The current closed-loop matrix is not ', ...
               'Schur stable. rho = %.12g.'], ...
               iteration,current_spectral_radius);

    end

    policy_stage_matrix = ...
        Q ...
        + L_previous'*RP*L_previous ...
        - K_previous'*RE*K_previous;

    policy_stage_matrix = ...
        0.5*(policy_stage_matrix+policy_stage_matrix');

    % MATLAB dlyap(A_iteration',S) solves:
    %
    %   A_iteration'*P*A_iteration - P + S = 0.

    P = dlyap(A_iteration',policy_stage_matrix);

    P = 0.5*(P+P');

    %% Policy Improvement

    H_PP = RP+B'*P*B;

    H_PE = B'*P*E;

    H_EP = E'*P*B;

    H_EE = E'*P*E-RE;

    H_Px = B'*P*Ad;

    H_Ex = E'*P*Ad;

    pursuer_update_matrix = ...
        H_PP-H_PE*(H_EE\H_EP);

    pursuer_update_vector = ...
        H_PE*(H_EE\H_Ex)-H_Px;

    evader_update_matrix = ...
        H_EE-H_EP*(H_PP\H_PE);

    evader_update_vector = ...
        H_EP*(H_PP\H_Px)-H_Ex;

    if rcond(pursuer_update_matrix) <= conditioning_tolerance

        error(['The pursuer policy update is poorly ', ...
               'conditioned at iteration %d.'],iteration);

    end

    if rcond(evader_update_matrix) <= conditioning_tolerance

        error(['The evader policy update is poorly ', ...
               'conditioned at iteration %d.'],iteration);

    end

    L = pursuer_update_matrix\pursuer_update_vector;

    K = evader_update_matrix\evader_update_vector;

    %% Iteration Diagnostics

    A_updated = Ad+B*L+E*K;

    P_error_history(iteration) = ...
        norm(P-P_previous,'fro');

    L_error_history(iteration) = ...
        norm(L-L_previous,'fro');

    K_error_history(iteration) = ...
        norm(K-K_previous,'fro');

    spectral_radius_history(iteration) = ...
        max(abs(eig(A_updated)));

    game_matrix_iteration = ...
        [RP+B'*P*B, B'*P*E;
         E'*P*B,    E'*P*E-RE];

    game_rcond_history(iteration) = ...
        rcond(game_matrix_iteration);

    %% Original Stopping Criterion

    if P_error_history(iteration) < policy_tolerance

        converged = true;

        break;

    end

end

%% Trim Histories

P_error_history = ...
    P_error_history(1:iteration);

L_error_history = ...
    L_error_history(1:iteration);

K_error_history = ...
    K_error_history(1:iteration);

spectral_radius_history = ...
    spectral_radius_history(1:iteration);

game_rcond_history = ...
    game_rcond_history(1:iteration);

if ~converged

    warning(['Policy iteration reached the maximum number ', ...
             'of iterations before convergence.']);

end

%% ============================================================
%  FINAL NOMINAL VERIFICATION
%  ============================================================

Acl = Ad+B*L+E*K;

closed_loop_spectral_radius = ...
    max(abs(eig(Acl)));

%% Final Game Blocks

H_PP = RP+B'*P*B;

H_PE = B'*P*E;

H_EP = E'*P*B;

H_EE = E'*P*E-RE;

H_Px = B'*P*Ad;

H_Ex = E'*P*Ad;

game_matrix = ...
    [H_PP, H_PE;
     H_EP, H_EE];

state_coupling = ...
    [H_Px;
     H_Ex];

game_matrix_rcond = ...
    rcond(game_matrix);

%% Saddle Curvature

pursuer_curvature_minimum = ...
    min(real(eig(0.5*(H_PP+H_PP'))));

evader_schur = ...
    H_EE-H_EP*(H_PP\H_PE);

evader_schur = ...
    0.5*(evader_schur+evader_schur');

evader_curvature_maximum = ...
    max(real(eig(evader_schur)));

%% GARE Residual

GARE_right_side = ...
    Q ...
    + Ad'*P*Ad ...
    - [Ad'*P*B,Ad'*P*E] ...
      *(game_matrix\state_coupling);

GARE_right_side = ...
    0.5*(GARE_right_side+GARE_right_side');

GARE_residual_matrix = ...
    P-GARE_right_side;

GARE_residual = ...
    norm(GARE_residual_matrix,'fro');

normalized_GARE_residual = ...
    GARE_residual/(1+norm(P,'fro'));

%% Lyapunov Residual

Lyapunov_right_side = ...
    Q ...
    + L'*RP*L ...
    - K'*RE*K ...
    + Acl'*P*Acl;

Lyapunov_right_side = ...
    0.5*(Lyapunov_right_side+Lyapunov_right_side');

Lyapunov_residual_matrix = ...
    P-Lyapunov_right_side;

Lyapunov_residual = ...
    norm(Lyapunov_residual_matrix,'fro');

normalized_Lyapunov_residual = ...
    Lyapunov_residual/(1+norm(P,'fro'));

%% Numerical Validity Checks

if closed_loop_spectral_radius >= 1

    error(['The converged closed-loop matrix is not ', ...
           'Schur stable. rho = %.12g.'], ...
           closed_loop_spectral_radius);

end

if pursuer_curvature_minimum <= 0

    error(['The minimizing-player curvature condition ', ...
           'is not satisfied.']);

end

if evader_curvature_maximum >= 0

    error(['The maximizing-player curvature condition ', ...
           'is not satisfied.']);

end

if game_matrix_rcond <= conditioning_tolerance

    error(['The converged game matrix is singular or ', ...
           'poorly conditioned.']);

end

%% Nominal Terminal Report

fprintf('\n');
fprintf('NOMINAL GARE POLICY-ITERATION RESULT\n');
fprintf('------------------------------------------------------------\n');
fprintf('Converged:                         %d\n',converged);
fprintf('Iterations:                        %d\n',iteration);
fprintf('Closed-loop spectral radius:       %.12e\n', ...
    closed_loop_spectral_radius);
fprintf('GARE residual:                     %.12e\n', ...
    GARE_residual);
fprintf('Normalized GARE residual:          %.12e\n', ...
    normalized_GARE_residual);
fprintf('Lyapunov residual:                 %.12e\n', ...
    Lyapunov_residual);
fprintf('Normalized Lyapunov residual:      %.12e\n', ...
    normalized_Lyapunov_residual);
fprintf('Game-matrix reciprocal condition:  %.12e\n', ...
    game_matrix_rcond);
fprintf('Pursuer curvature minimum:         %.12e\n', ...
    pursuer_curvature_minimum);
fprintf('Evader curvature maximum:          %.12e\n', ...
    evader_curvature_maximum);

fprintf('\nPursuer/Minimizing Gain L:\n');
disp(L);

fprintf('Evader/Maximizing Gain K:\n');
disp(K);

fprintf('Value Matrix P:\n');
disp(P);

%% ============================================================
%  DISTURBANCE-AWARE THESIS EXTENSION
%  ============================================================

bP_true = 0.040;

bE_true = -0.010;

bias_true = ...
    [bP_true;
     bE_true];

%% Calibration Settings

Ncal = 250;

calibration_noise_std = 1e-4;

x_cal = zeros(nx,Ncal+1);

x_cal(:,1) = ...
    [0.6;
    -0.3;
     0.2];

transition_residual = ...
    zeros(nx,Ncal);

%% Model-Based Calibration Experiment

for calibration_index = 1:Ncal

    uP_calibration_command = ...
        0.35*sin(0.17*calibration_index) ...
        + 0.18*cos(0.41*calibration_index);

    uE_calibration_command = ...
        0.30*cos(0.13*calibration_index) ...
        - 0.15*sin(0.37*calibration_index);

    uP_calibration_actual = ...
        uP_calibration_command+bP_true;

    uE_calibration_actual = ...
        uE_calibration_command+bE_true;

    calibration_noise = ...
        calibration_noise_std*randn(nx,1);

    x_cal(:,calibration_index+1) = ...
        Ad*x_cal(:,calibration_index) ...
        + B*uP_calibration_actual ...
        + E*uE_calibration_actual ...
        + calibration_noise;

    transition_residual(:,calibration_index) = ...
        x_cal(:,calibration_index+1) ...
        - Ad*x_cal(:,calibration_index) ...
        - B*uP_calibration_command ...
        - E*uE_calibration_command;

end

%% Offset Identifiability

bias_map = [B E];

bias_map_rank = rank(bias_map);

required_bias_rank = nuP+nuE;

bias_map_condition_number = ...
    cond(bias_map);

if bias_map_rank < required_bias_rank

    error(['The pursuer and evader offsets are not ', ...
           'separately identifiable from [B E].']);

end

%% Primary Offset Estimate

mean_transition_residual = ...
    mean(transition_residual,2);

bias_estimate = ...
    bias_map\mean_transition_residual;

bP_hat = bias_estimate(1);

bE_hat = bias_estimate(2);

bias_estimation_error = ...
    bias_estimate-bias_true;

bP_absolute_error = ...
    abs(bP_hat-bP_true);

bE_absolute_error = ...
    abs(bE_hat-bE_true);

bP_percentage_error = ...
    100*bP_absolute_error/max(abs(bP_true),eps);

bE_percentage_error = ...
    100*bE_absolute_error/max(abs(bE_true),eps);

%% Theoretical Estimator Covariance
%
% For independent isotropic calibration noise:
%
%   Cov(b_hat)
%       = sigma^2/Ncal * (M'*M)^(-1),
%
% where:
%
%   M = [B E].

theoretical_bias_covariance = ...
    (calibration_noise_std^2/Ncal) ...
    *((bias_map'*bias_map)\eye(required_bias_rank));

theoretical_bias_covariance = ...
    0.5*(theoretical_bias_covariance ...
    + theoretical_bias_covariance');

theoretical_bias_std = ...
    sqrt(max(diag(theoretical_bias_covariance),0));

theoretical_bias_95_lower = ...
    bias_true-1.96*theoretical_bias_std;

theoretical_bias_95_upper = ...
    bias_true+1.96*theoretical_bias_std;

%% Monte Carlo Estimator Validation

monte_carlo_trials = 1000;

mean_noise_trials = ...
    calibration_noise_std/sqrt(Ncal) ...
    *randn(nx,monte_carlo_trials);

mean_residual_trials = ...
    repmat( ...
    bias_map*bias_true, ...
    1,monte_carlo_trials) ...
    + mean_noise_trials;

monte_carlo_bias_estimates = ...
    bias_map\mean_residual_trials;

monte_carlo_bias_mean = ...
    mean(monte_carlo_bias_estimates,2);

% Standard deviation of individual calibration estimates.

monte_carlo_bias_std = ...
    std(monte_carlo_bias_estimates,0,2);

% Standard error of the reported Monte Carlo sample mean.

monte_carlo_mean_standard_error = ...
    monte_carlo_bias_std/sqrt(monte_carlo_trials);

monte_carlo_bias_error = ...
    monte_carlo_bias_mean-bias_true;

monte_carlo_bias_rmse = ...
    sqrt(mean( ...
    (monte_carlo_bias_estimates ...
    - repmat( ...
    bias_true,1,monte_carlo_trials)).^2,2));

