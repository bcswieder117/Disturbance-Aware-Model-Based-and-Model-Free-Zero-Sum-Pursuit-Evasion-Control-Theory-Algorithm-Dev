    for time_index = 1:Nsim

        analytical_deviation(:,time_index+1,case_index) = ...
            Acl*analytical_deviation(:,time_index,case_index) ...
            + affine_forcing(:,case_index);

    end

end

analytical_mismatch = ...
    zeros(Nsim+1,number_of_cases);

maximum_analytical_mismatch = ...
    zeros(number_of_cases,1);

for case_index = 1:number_of_cases

    for time_index = 1:Nsim+1

        mismatch_vector = ...
            state_deviation(:,time_index,case_index) ...
            - analytical_deviation(:,time_index,case_index);

        analytical_mismatch(time_index,case_index) = ...
            norm(mismatch_vector,2);

    end

    maximum_analytical_mismatch(case_index) = ...
        max(analytical_mismatch(:,case_index));

end

