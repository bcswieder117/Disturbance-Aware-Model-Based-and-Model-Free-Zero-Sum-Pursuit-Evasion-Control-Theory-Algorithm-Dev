font_name = 'Times New Roman';
font_size = 11;

set(groot,'defaultFigureColor','w');
set(groot,'defaultFigureInvertHardcopy','off');

set(groot,'defaultAxesColor','w');
set(groot,'defaultAxesXColor','k');
set(groot,'defaultAxesYColor','k');
set(groot,'defaultAxesFontName',font_name);
set(groot,'defaultAxesFontSize',font_size);
set(groot,'defaultAxesTickLabelInterpreter','none');

set(groot,'defaultTextFontName',font_name);
set(groot,'defaultTextFontSize',font_size);
set(groot,'defaultTextColor','k');
set(groot,'defaultTextInterpreter','none');

set(groot,'defaultLegendFontName',font_name);
set(groot,'defaultLegendFontSize',10);
set(groot,'defaultLegendInterpreter','none');

nominal_color = [0.0000 0.4470 0.7410];
uncompensated_color = [0.8500 0.3250 0.0980];
estimated_color = [0.4660 0.6740 0.1880];
exact_color = [0.4940 0.1840 0.5560];
reference_color = [0.3010 0.7450 0.9330];

grid_color = [0.72 0.72 0.72];
annotation_edge_color = [0.45 0.45 0.45];

plot_floor = 1e-16;

%% ========================================================================
% 3. COMMON DISCRETE-TIME ZERO-SUM BENCHMARK
%% ========================================================================

Ad = [ ...
    0.906488   0.0816012  -0.0005;
    0.0741349  0.90121    -0.000708383;
    0           0          0.132655];

B = [ ...
    -0.00150808;
    -0.0096;
     0.867345];

E = [ ...
    0.00951892;
    0.00038373;
    0];

C = [1 0 -1]; %#ok<NASGU>

nx = size(Ad,1);
nuP = size(B,2);
nuE = size(E,2);

if nuP ~= 1 || nuE ~= 1

    error([ ...
        'This implementation assumes one scalar pursuer input ' ...
        'and one scalar evader input.']);

end

%% ========================================================================
% 4. COMMON ZERO-SUM COST
%% ========================================================================

Q = eye(nx);

gamma = 1;

RP = 1;
RE = gamma^2;

%% ========================================================================
% 5. MODEL-BASED GARE VALUE ITERATION
%% ========================================================================

tolerance = 1e-10;
maximum_iterations = 5000;

P = eye(nx);

L = zeros(nuP,nx);
K = zeros(nuE,nx);

P_change_history = nan(maximum_iterations,1);
L_change_history = nan(maximum_iterations,1);
K_change_history = nan(maximum_iterations,1);

spectral_radius_history = nan(maximum_iterations,1);
game_rcond_history = nan(maximum_iterations,1);

pursuer_curvature_history = nan(maximum_iterations,1);
evader_curvature_history = nan(maximum_iterations,1);

converged = false;

for iteration = 1:maximum_iterations

    P_old = P;
    L_old = L;
    K_old = K;

    HPP_old = RP+B'*P_old*B;
    HPE_old = B'*P_old*E;
    HEP_old = E'*P_old*B;
    HEE_old = E'*P_old*E-RE;

    game_matrix_old = [ ...
        HPP_old HPE_old;
        HEP_old HEE_old];

    state_cross_old = [ ...
        B'*P_old*Ad;
        E'*P_old*Ad];

    current_game_rcond = rcond(game_matrix_old);

    if ~isfinite(current_game_rcond) || ...
       current_game_rcond < 1e-13

        error([ ...
            'GARE value iteration failed because the current ' ...
            'game matrix is numerically singular.']);

    end

    current_pursuer_curvature = HPP_old;

    current_evader_curvature = ...
        HEE_old-HEP_old*(HPP_old\HPE_old);

    if current_pursuer_curvature <= 0

        error([ ...
            'GARE value iteration left the positive-curvature ' ...
            'pursuer region.']);

    end

    if current_evader_curvature >= 0

        error([ ...
            'GARE value iteration left the negative-curvature ' ...
            'evader region.']);

    end

    P_new = Q+Ad'*P_old*Ad ...
        - [Ad'*P_old*B Ad'*P_old*E] ...
          *(game_matrix_old\state_cross_old);

    P_new = 0.5*(P_new+P_new');

    if any(~isfinite(P_new(:)))

        error( ...
            'The updated GARE value matrix contains NaN or Inf values.');

    end

    HPP_new = RP+B'*P_new*B;
    HPE_new = B'*P_new*E;
    HEP_new = E'*P_new*B;
    HEE_new = E'*P_new*E-RE;

    game_matrix_new = [ ...
        HPP_new HPE_new;
        HEP_new HEE_new];

    state_cross_new = [ ...
        B'*P_new*Ad;
        E'*P_new*Ad];

    updated_game_rcond = rcond(game_matrix_new);

    if ~isfinite(updated_game_rcond) || ...
       updated_game_rcond < 1e-13

        error([ ...
            'Gain recovery failed because the updated game matrix ' ...
            'is numerically singular.']);

    end

    updated_pursuer_curvature = HPP_new;

    updated_evader_curvature = ...
        HEE_new-HEP_new*(HPP_new\HPE_new);

    if updated_pursuer_curvature <= 0

        error([ ...
            'The updated value matrix does not produce positive ' ...
            'pursuer curvature.']);

    end

    if updated_evader_curvature >= 0

        error([ ...
            'The updated value matrix does not produce negative ' ...
            'evader curvature.']);

    end

    gain_pair_new = -(game_matrix_new\state_cross_new);

    L_new = gain_pair_new(1:nuP,:);
    K_new = gain_pair_new(nuP+1:nuP+nuE,:);

    if any(~isfinite(L_new(:))) || ...
       any(~isfinite(K_new(:)))

        error([ ...
            'The recovered pursuer or evader gain contains ' ...
            'NaN or Inf values.']);

    end

    P_change_history(iteration) = ...
        norm(P_new-P_old,'fro');

    L_change_history(iteration) = ...
        norm(L_new-L_old,'fro');

    K_change_history(iteration) = ...
        norm(K_new-K_old,'fro');

    Acl_iteration = ...
        Ad+B*L_new+E*K_new;

    spectral_radius_history(iteration) = ...
        max(abs(eig(Acl_iteration)));

    game_rcond_history(iteration) = ...
        updated_game_rcond;

    pursuer_curvature_history(iteration) = ...
        updated_pursuer_curvature;

    evader_curvature_history(iteration) = ...
        updated_evader_curvature;

    P = P_new;
    L = L_new;
    K = K_new;

    if P_change_history(iteration) < tolerance && ...
       L_change_history(iteration) < tolerance && ...
       K_change_history(iteration) < tolerance

        converged = true;
        break;

    end

end

iteration_count = iteration;

P_change_history = ...
    P_change_history(1:iteration_count);

L_change_history = ...
    L_change_history(1:iteration_count);

K_change_history = ...
    K_change_history(1:iteration_count);

spectral_radius_history = ...
    spectral_radius_history(1:iteration_count);

game_rcond_history = ...
    game_rcond_history(1:iteration_count);

pursuer_curvature_history = ...
    pursuer_curvature_history(1:iteration_count);

evader_curvature_history = ...
    evader_curvature_history(1:iteration_count);

if ~converged

    warning([ ...
        'GARE value iteration reached the maximum iteration count ' ...
        'before all convergence criteria were satisfied.']);

end

%% ========================================================================
% 6. FINAL NOMINAL NUMERICAL CHECKS
%% ========================================================================

Acl = Ad+B*L+E*K;

closed_loop_radius = ...
    max(abs(eig(Acl)));

if ~isfinite(closed_loop_radius)

    error( ...
        'The final closed-loop spectral radius is not finite.');

end

if closed_loop_radius >= 1

    error([ ...
        'The converged GARE value-iteration gain pair does not ' ...
        'produce a Schur-stable closed loop.']);

end

HPP = RP+B'*P*B;
HPE = B'*P*E;
HEP = E'*P*B;
HEE = E'*P*E-RE;

game_matrix = [ ...
    HPP HPE;
    HEP HEE];

state_cross = [ ...
    B'*P*Ad;
    E'*P*Ad];

game_matrix_rcond = ...
    rcond(game_matrix);

if ~isfinite(game_matrix_rcond) || ...
   game_matrix_rcond < 1e-13

    error( ...
        'The final GARE game matrix is numerically singular.');

end

riccati_right_side = ...
    Q+Ad'*P*Ad ...
    - [Ad'*P*B Ad'*P*E] ...
      *(game_matrix\state_cross);

gare_residual_matrix = ...
    P-riccati_right_side;

gare_residual = ...
    norm(gare_residual_matrix,'fro');

normalized_gare_residual = ...
    gare_residual/(1+norm(P,'fro'));

lyapunov_right_side = ...
    Q ...
    + L'*RP*L ...
    - K'*RE*K ...
    + Acl'*P*Acl;

lyapunov_residual_matrix = ...
    P-lyapunov_right_side;

lyapunov_residual = ...
    norm(lyapunov_residual_matrix,'fro');

normalized_lyapunov_residual = ...
    lyapunov_residual/(1+norm(P,'fro'));

P_symmetry_error = ...
    norm(P-P','fro');

minimum_P_eigenvalue = ...
    min(real(eig(P)));

pursuer_curvature_minimum = ...
    HPP;

evader_curvature_maximum = ...
    HEE-HEP*(HPP\HPE);

if pursuer_curvature_minimum <= 0

    error( ...
        'The final pursuer direction is not strictly convex.');

end

if evader_curvature_maximum >= 0

    error( ...
        'The final evader direction is not strictly concave.');

end

%% ========================================================================
% 7. PERSISTENT COMMAND-CHANNEL OFFSETS
%% ========================================================================

bP_true = 0.040;
bE_true = -0.010;

bias_true = [ ...
    bP_true;
    bE_true];

%% ========================================================================
% 8. MODEL-BASED TRANSITION-RESIDUAL CALIBRATION
%% ========================================================================

Ncal = 250;

calibration_noise_standard_deviation = ...
    1e-4;

x_calibration = zeros(nx,Ncal+1);

x_calibration(:,1) = [ ...
     0.6;
    -0.3;
     0.2];

transition_residual = ...
    zeros(nx,Ncal);

uP_calibration_command = ...
    zeros(1,Ncal);

uE_calibration_command = ...
    zeros(1,Ncal);

for k = 1:Ncal

    uP_calibration_command(k) = ...
        0.35*sin(0.17*k) ...
        + 0.18*cos(0.41*k);

    uE_calibration_command(k) = ...
        0.30*cos(0.13*k) ...
        - 0.15*sin(0.37*k);

    uP_calibration_actual = ...
        uP_calibration_command(k)+bP_true;

    uE_calibration_actual = ...
        uE_calibration_command(k)+bE_true;

    x_calibration(:,k+1) = ...
        Ad*x_calibration(:,k) ...
        + B*uP_calibration_actual ...
        + E*uE_calibration_actual ...
        + calibration_noise_standard_deviation ...
          *randn(nx,1);

    transition_residual(:,k) = ...
        x_calibration(:,k+1) ...
        - Ad*x_calibration(:,k) ...
        - B*uP_calibration_command(k) ...
        - E*uE_calibration_command(k);

end

bias_map = [B E];

bias_map_rank = ...
    rank(bias_map);

bias_map_condition_number = ...
    cond(bias_map);

if bias_map_rank < nuP+nuE

    error([ ...
        'The pursuer and evader command offsets are not ' ...
        'separately identifiable from the matrix [B E].']);

end

bias_estimate = ...
    bias_map\mean(transition_residual,2);

bP_hat = bias_estimate(1);
bE_hat = bias_estimate(2);

bias_estimation_error = ...
    bias_estimate-bias_true;

pursuer_bias_absolute_error = ...
    abs(bP_hat-bP_true);

evader_bias_absolute_error = ...
    abs(bE_hat-bE_true);

pursuer_bias_percent_error = ...
    100*abs((bP_hat-bP_true)/bP_true);

evader_bias_percent_error = ...
    100*abs((bE_hat-bE_true)/bE_true);

%% ========================================================================
% 9. ESTIMATOR COVARIANCE AND MONTE CARLO VALIDATION
%% ========================================================================

bias_covariance_theoretical = ...
    (calibration_noise_standard_deviation^2/Ncal) ...
    *((bias_map'*bias_map)\eye(nuP+nuE));

bias_standard_deviation_theoretical = ...
    sqrt(diag(bias_covariance_theoretical));

Nmc = 1000;

bias_estimates_monte_carlo = ...
    zeros(2,Nmc);

for trial = 1:Nmc

    residual_monte_carlo = ...
        bias_map*bias_true ...
        + calibration_noise_standard_deviation ...
          *randn(nx,Ncal);

    bias_estimates_monte_carlo(:,trial) = ...
        bias_map\mean(residual_monte_carlo,2);

end

bias_monte_carlo_mean = ...
    mean(bias_estimates_monte_carlo,2);

bias_monte_carlo_standard_deviation = ...
    std(bias_estimates_monte_carlo,0,2);

bias_monte_carlo_rmse = ...
    sqrt(mean( ...
    (bias_estimates_monte_carlo-bias_true).^2,2));

bias_monte_carlo_mean_standard_error = ...
    bias_monte_carlo_standard_deviation/sqrt(Nmc);

bias_theoretical_lower_95 = ...
    bias_true ...
    - 1.96*bias_standard_deviation_theoretical;

bias_theoretical_upper_95 = ...
    bias_true ...
    + 1.96*bias_standard_deviation_theoretical;

%% ========================================================================
% 10. FOUR MATCHED CLOSED-LOOP EXECUTION CASES
%% ========================================================================

Nsim = 500;

x_initial = [ ...
    10;
     5;
    -2];

x_nominal = zeros(nx,Nsim+1);
x_uncompensated = zeros(nx,Nsim+1);
x_estimated_compensation = zeros(nx,Nsim+1);
x_exact_compensation = zeros(nx,Nsim+1);

x_nominal(:,1) = x_initial;
x_uncompensated(:,1) = x_initial;
x_estimated_compensation(:,1) = x_initial;
x_exact_compensation(:,1) = x_initial;

stage_cost_nominal = zeros(1,Nsim);
stage_cost_uncompensated = zeros(1,Nsim);
stage_cost_estimated = zeros(1,Nsim);
stage_cost_exact = zeros(1,Nsim);

uP_policy_nominal = zeros(1,Nsim);
uP_command_nominal = zeros(1,Nsim);
uP_actual_nominal = zeros(1,Nsim);

uP_policy_uncompensated = zeros(1,Nsim);
uP_command_uncompensated = zeros(1,Nsim);
uP_actual_uncompensated = zeros(1,Nsim);

uP_policy_estimated = zeros(1,Nsim);
uP_command_estimated = zeros(1,Nsim);
uP_actual_estimated = zeros(1,Nsim);

uP_policy_exact = zeros(1,Nsim);
uP_command_exact = zeros(1,Nsim);
uP_actual_exact = zeros(1,Nsim);

uE_policy_nominal = zeros(1,Nsim);
uE_command_nominal = zeros(1,Nsim);
uE_actual_nominal = zeros(1,Nsim);

uE_policy_uncompensated = zeros(1,Nsim);
uE_command_uncompensated = zeros(1,Nsim);
uE_actual_uncompensated = zeros(1,Nsim);

uE_policy_estimated = zeros(1,Nsim);
uE_command_estimated = zeros(1,Nsim);
uE_actual_estimated = zeros(1,Nsim);

uE_policy_exact = zeros(1,Nsim);
uE_command_exact = zeros(1,Nsim);
uE_actual_exact = zeros(1,Nsim);

for k = 1:Nsim

    uP_policy_nominal(k) = ...
        L*x_nominal(:,k);

    uE_policy_nominal(k) = ...
        K*x_nominal(:,k);

    uP_command_nominal(k) = ...
        uP_policy_nominal(k);

    uE_command_nominal(k) = ...
        uE_policy_nominal(k);

    uP_actual_nominal(k) = ...
        uP_command_nominal(k);

    uE_actual_nominal(k) = ...
        uE_command_nominal(k);

    x_nominal(:,k+1) = ...
        Ad*x_nominal(:,k) ...
        + B*uP_actual_nominal(k) ...
        + E*uE_actual_nominal(k);

    stage_cost_nominal(k) = ...
        x_nominal(:,k)'*Q*x_nominal(:,k) ...
        + uP_actual_nominal(k)' ...
          *RP*uP_actual_nominal(k) ...
        - uE_actual_nominal(k)' ...
          *RE*uE_actual_nominal(k);

    uP_policy_uncompensated(k) = ...
        L*x_uncompensated(:,k);

    uE_policy_uncompensated(k) = ...
        K*x_uncompensated(:,k);

    uP_command_uncompensated(k) = ...
        uP_policy_uncompensated(k);

    uE_command_uncompensated(k) = ...
        uE_policy_uncompensated(k);

    uP_actual_uncompensated(k) = ...
        uP_command_uncompensated(k)+bP_true;

    uE_actual_uncompensated(k) = ...
        uE_command_uncompensated(k)+bE_true;

    x_uncompensated(:,k+1) = ...
        Ad*x_uncompensated(:,k) ...
        + B*uP_actual_uncompensated(k) ...
        + E*uE_actual_uncompensated(k);

    stage_cost_uncompensated(k) = ...
        x_uncompensated(:,k)' ...
        *Q*x_uncompensated(:,k) ...
        + uP_actual_uncompensated(k)' ...
          *RP*uP_actual_uncompensated(k) ...
        - uE_actual_uncompensated(k)' ...
          *RE*uE_actual_uncompensated(k);

    uP_policy_estimated(k) = ...
        L*x_estimated_compensation(:,k);

    uE_policy_estimated(k) = ...
        K*x_estimated_compensation(:,k);

    uP_command_estimated(k) = ...
        uP_policy_estimated(k)-bP_hat;

    uE_command_estimated(k) = ...
        uE_policy_estimated(k)-bE_hat;

    uP_actual_estimated(k) = ...
        uP_command_estimated(k)+bP_true;

    uE_actual_estimated(k) = ...
        uE_command_estimated(k)+bE_true;

    x_estimated_compensation(:,k+1) = ...
        Ad*x_estimated_compensation(:,k) ...
        + B*uP_actual_estimated(k) ...
        + E*uE_actual_estimated(k);

    stage_cost_estimated(k) = ...
        x_estimated_compensation(:,k)' ...
        *Q*x_estimated_compensation(:,k) ...
        + uP_actual_estimated(k)' ...
          *RP*uP_actual_estimated(k) ...
        - uE_actual_estimated(k)' ...
          *RE*uE_actual_estimated(k);

    uP_policy_exact(k) = ...
        L*x_exact_compensation(:,k);

    uE_policy_exact(k) = ...
        K*x_exact_compensation(:,k);

    uP_command_exact(k) = ...
        uP_policy_exact(k)-bP_true;

    uE_command_exact(k) = ...
        uE_policy_exact(k)-bE_true;

    uP_actual_exact(k) = ...
        uP_command_exact(k)+bP_true;

    uE_actual_exact(k) = ...
        uE_command_exact(k)+bE_true;

    x_exact_compensation(:,k+1) = ...
        Ad*x_exact_compensation(:,k) ...
        + B*uP_actual_exact(k) ...
        + E*uE_actual_exact(k);

    stage_cost_exact(k) = ...
        x_exact_compensation(:,k)' ...
        *Q*x_exact_compensation(:,k) ...
        + uP_actual_exact(k)' ...
          *RP*uP_actual_exact(k) ...
        - uE_actual_exact(k)' ...
          *RE*uE_actual_exact(k);

end

%% ========================================================================
% 11. ANALYTICAL AFFINE-DEVIATION VERIFICATION
%% ========================================================================

uncompensated_affine_forcing = ...
    B*bP_true+E*bE_true;

estimated_affine_forcing = ...
    B*(bP_true-bP_hat) ...
    + E*(bE_true-bE_hat);

deviation_uncompensated_analytical = ...
    zeros(nx,Nsim+1);

deviation_estimated_analytical = ...
    zeros(nx,Nsim+1);

for k = 1:Nsim

    deviation_uncompensated_analytical(:,k+1) = ...
        Acl*deviation_uncompensated_analytical(:,k) ...
        + uncompensated_affine_forcing;

    deviation_estimated_analytical(:,k+1) = ...
        Acl*deviation_estimated_analytical(:,k) ...
        + estimated_affine_forcing;

end

deviation_uncompensated_simulated = ...
    x_uncompensated-x_nominal;

deviation_estimated_simulated = ...
    x_estimated_compensation-x_nominal;

uncompensated_analytical_mismatch = ...
    max(vecnorm( ...
    deviation_uncompensated_simulated ...
    - deviation_uncompensated_analytical, ...
    2,1));

estimated_analytical_mismatch = ...
    max(vecnorm( ...
    deviation_estimated_simulated ...
    - deviation_estimated_analytical, ...
    2,1));

maximum_exact_trajectory_error = ...
    max(vecnorm( ...
    x_exact_compensation-x_nominal, ...
    2,1));

predicted_uncompensated_equilibrium = ...
    (eye(nx)-Acl)\uncompensated_affine_forcing;

predicted_estimated_equilibrium = ...
    (eye(nx)-Acl)\estimated_affine_forcing;

predicted_uncompensated_equilibrium_norm = ...
    norm(predicted_uncompensated_equilibrium);

predicted_estimated_equilibrium_norm = ...
    norm(predicted_estimated_equilibrium);

%% ========================================================================
% 12. COST-WEIGHTED STATE-DEVIATION METRICS
%% ========================================================================

weighted_deviation_uncompensated = ...
    zeros(1,Nsim+1);

weighted_deviation_estimated = ...
    zeros(1,Nsim+1);

weighted_deviation_exact = ...
    zeros(1,Nsim+1);

for k = 1:Nsim+1

    deviation_uncompensated = ...
        x_uncompensated(:,k)-x_nominal(:,k);

    deviation_estimated = ...
        x_estimated_compensation(:,k)-x_nominal(:,k);

    deviation_exact = ...
        x_exact_compensation(:,k)-x_nominal(:,k);

