%% ========================================================================
% Ch4 Extended_GARE_VI.m
%
% Disturbance-aware extension of the supplied GARE_VI.m program.
%
% The original model-based GARE value-iteration structure is retained:
%   1. Update the value matrix through the GARE Riccati mapping.
%   2. Recover the pursuer and evader gains from the updated value matrix.
%   3. Repeat until the value matrix and both gains converge.
%
% The thesis extension adds:
%   1. Persistent pursuer and evader command-channel offsets.
%   2. Known-model transition-residual offset estimation.
%   3. Affine command compensation.
%   4. Nominal, uncompensated, estimated-compensation, and
%      exact-compensation execution cases.
%   5. Monte Carlo estimator validation.
%   6. Numerical diagnostics, CSV files, MAT data, a text report,
%      and publication-quality figures.
%
% B is the pursuer or minimizing-player input channel.
% E is the evader or maximizing-player input channel.
%% ========================================================================

clear;
close all;
clc;

rng(7,'twister');
format long g;

