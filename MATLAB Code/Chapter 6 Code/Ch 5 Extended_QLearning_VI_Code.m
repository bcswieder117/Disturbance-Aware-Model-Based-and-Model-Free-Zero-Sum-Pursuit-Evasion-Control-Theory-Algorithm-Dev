%% ========================================================================
% 3. COMMON DISCRETE-TIME ZERO-SUM BENCHMARK
%% ========================================================================

Ad = [ ...
    0.906488   0.0816012  -0.0005;
    0.0741349  0.90121    -0.000708383;
    0           0          0.132655];

B = [ ...
    -0.00150808;
    -0.0096;
     0.867345];

E = [ ...
    0.00951892;
    0.00038373;
    0];

C = [1 0 -1]; %#ok<NASGU>

nx = size(Ad,1);
nuP = size(B,2);
nuE = size(E,2);

if nuP ~= 1 || nuE ~= 1

    error([ ...
        'This implementation assumes one scalar pursuer input ' ...
        'and one scalar evader input.']);

end

%% ========================================================================
% 4. COMMON ZERO-SUM COST
%% ========================================================================

Q = eye(nx);

gamma = 1;

RP = 1;
RE = gamma^2;

%% ========================================================================
% 5. PERSISTENT COMMAND-CHANNEL OFFSETS
%% ========================================================================

bP_true = 0.040;
bE_true = -0.010;

bias_true = [ ...
    bP_true;
    bE_true];

%% ========================================================================
% 6. MODEL-FREE COMMAND-MISMATCH CALIBRATION
%% ========================================================================

Ncal = 250;

input_measurement_noise_standard_deviation = ...
    1e-4;

uP_calibration_command = ...
    zeros(1,Ncal);

uE_calibration_command = ...
    zeros(1,Ncal);

uP_calibration_measured = ...
    zeros(1,Ncal);

uE_calibration_measured = ...
    zeros(1,Ncal);

for k = 1:Ncal

    uP_calibration_command(k) = ...
        0.35*sin(0.17*k) ...
        + 0.18*cos(0.41*k);

    uE_calibration_command(k) = ...
        0.30*cos(0.13*k) ...
        - 0.15*sin(0.37*k);

    uP_calibration_measured(k) = ...
        uP_calibration_command(k) ...
        + bP_true ...
        + input_measurement_noise_standard_deviation*randn;

    uE_calibration_measured(k) = ...
        uE_calibration_command(k) ...
        + bE_true ...
        + input_measurement_noise_standard_deviation*randn;

end

pursuer_command_mismatch = ...
    uP_calibration_measured-uP_calibration_command;

evader_command_mismatch = ...
    uE_calibration_measured-uE_calibration_command;

bP_hat = ...
    mean(pursuer_command_mismatch);

bE_hat = ...
    mean(evader_command_mismatch);

bias_estimate = [ ...
    bP_hat;
    bE_hat];

bias_estimation_error = ...
    bias_estimate-bias_true;

pursuer_bias_absolute_error = ...
    abs(bP_hat-bP_true);

evader_bias_absolute_error = ...
    abs(bE_hat-bE_true);

pursuer_bias_percent_error = ...
    100*abs((bP_hat-bP_true)/bP_true);

evader_bias_percent_error = ...
    100*abs((bE_hat-bE_true)/bE_true);

bias_standard_deviation_theoretical = ...
    (input_measurement_noise_standard_deviation/sqrt(Ncal)) ...
    *ones(2,1);

bias_theoretical_lower_95 = ...
    bias_true ...
    - 1.96*bias_standard_deviation_theoretical;

bias_theoretical_upper_95 = ...
    bias_true ...
    + 1.96*bias_standard_deviation_theoretical;

%% ========================================================================
% 7. MONTE CARLO VALIDATION OF THE MODEL-FREE ESTIMATOR
%% ========================================================================

Nmc = 1000;

bias_estimates_monte_carlo = ...
    zeros(2,Nmc);

for trial = 1:Nmc

    pursuer_noise_trial = ...
        input_measurement_noise_standard_deviation ...
        *randn(1,Ncal);

    evader_noise_trial = ...
        input_measurement_noise_standard_deviation ...
        *randn(1,Ncal);

    bias_estimates_monte_carlo(1,trial) = ...
        bP_true+mean(pursuer_noise_trial);

    bias_estimates_monte_carlo(2,trial) = ...
        bE_true+mean(evader_noise_trial);

end

bias_monte_carlo_mean = ...
    mean(bias_estimates_monte_carlo,2);

bias_monte_carlo_standard_deviation = ...
    std(bias_estimates_monte_carlo,0,2);

bias_monte_carlo_rmse = ...
    sqrt(mean( ...
    (bias_estimates_monte_carlo-bias_true).^2,2));

bias_monte_carlo_mean_standard_error = ...
    bias_monte_carlo_standard_deviation/sqrt(Nmc);

%% ========================================================================
% 8. FIXED DISTURBANCE-CONSISTENT OFF-POLICY DATASET
%% ========================================================================

Ndata = 500;

nz = nx+nuP+nuE;

number_of_features = ...
    nz*(nz+1)/2;

x_data = ...
    zeros(nx,Ndata+1);

x_data(:,1) = [ ...
    10;
     5;
    -2];

uP_data_desired_realized = ...
    zeros(1,Ndata);

uE_data_desired_realized = ...
    zeros(1,Ndata);

uP_data_command = ...
    zeros(1,Ndata);

uE_data_command = ...
    zeros(1,Ndata);

uP_data_actual = ...
    zeros(1,Ndata);

uE_data_actual = ...
    zeros(1,Ndata);

stage_cost_data = ...
    zeros(Ndata,1);

Phi_data = ...
    zeros(Ndata,number_of_features);

data_transition_mismatch = ...
    zeros(1,Ndata);

for k = 1:Ndata

    uP_data_desired_realized(k) = ...
        0.1*( ...
        2.0*sin(1.009*k) ...
        + 3.0*cos(0.538*k)^2 ...
        + 4.0*sin(0.900*k) ...
        + 5.0*cos(100.0*k) ...
        + 1.5*sin(2.109*k) ...
        + 1.4*cos(0.738*k)^2 ...
        + 2.4*sin(0.250*k) ...
        + 3.2*cos(80.0*k) ...
        + 1.2*sin(33.0*k));

    uE_data_desired_realized(k) = ...
        0.1*( ...
        0.2*sin(3.009*k) ...
        + 1.1*sin(3.109*k) ...
        + 0.4*cos(0.338*k)^2 ...
        + 1.9*sin(0.735*k) ...
        + 2.2*cos(66.0*k) ...
        + 2.3*sin(44.0*k));

    % Convert the desired realized exploration values into commands.
    uP_data_command(k) = ...
        uP_data_desired_realized(k)-bP_hat;

    uE_data_command(k) = ...
        uE_data_desired_realized(k)-bE_hat;

    % Persistent offsets act after the commands are formed.
    uP_data_actual(k) = ...
        uP_data_command(k)+bP_true;

    uE_data_actual(k) = ...
        uE_data_command(k)+bE_true;

    % The matrices are used by the simulation environment only.
    x_data(:,k+1) = ...
        Ad*x_data(:,k) ...
        + B*uP_data_actual(k) ...
        + E*uE_data_actual(k);

    z_data = [ ...
        x_data(:,k);
        uP_data_actual(k);
        uE_data_actual(k)];

    feature_index = 0;

    for row_index = 1:nz

        for column_index = row_index:nz

            feature_index = ...
                feature_index+1;

            if row_index == column_index

                Phi_data(k,feature_index) = ...
                    z_data(row_index)*z_data(column_index);

            else

                Phi_data(k,feature_index) = ...
                    2*z_data(row_index)*z_data(column_index);

            end

        end

    end

    stage_cost_data(k) = ...
        x_data(:,k)'*Q*x_data(:,k) ...
        + uP_data_actual(k)'*RP*uP_data_actual(k) ...
        - uE_data_actual(k)'*RE*uE_data_actual(k);

    reconstructed_successor = ...
        Ad*x_data(:,k) ...
        + B*uP_data_actual(k) ...
        + E*uE_data_actual(k);

    data_transition_mismatch(k) = ...
        norm( ...
        x_data(:,k+1)-reconstructed_successor, ...
        2);

end

maximum_data_transition_mismatch = ...
    max(data_transition_mismatch);

data_feature_rank = ...
    rank(Phi_data);

if data_feature_rank < number_of_features

    error([ ...
        'The fixed Q-learning feature matrix is not full column rank. ' ...
        'Increase or redesign the joint exploration signals.']);

end

data_feature_condition = ...
    cond(Phi_data);

data_column_scale = ...
    sqrt(sum(Phi_data.^2,1));

data_column_scale( ...
    data_column_scale < eps) = 1;

Phi_data_scaled = ...
    Phi_data./data_column_scale;

scaled_data_feature_rank = ...
    rank(Phi_data_scaled);

scaled_data_feature_condition = ...
    cond(Phi_data_scaled);

if scaled_data_feature_rank < number_of_features

    error([ ...
        'The scaled Q-learning feature matrix is not full column rank.']);

end

%% ========================================================================
% 9. MODEL-FREE Q-LEARNING VALUE ITERATION
%% ========================================================================

learning_tolerance = 1e-10;
maximum_learning_iterations = 5000;

% The initial matrix represents the one-stage zero-sum cost and supplies a
% valid convex-concave input block with zero initial feedback gains.
H = blkdiag( ...
    Q, ...
    RP, ...
    -RE);

H = 0.5*(H+H');

L = zeros(nuP,nx);
K = zeros(nuE,nx);

theta = ...
    zeros(number_of_features,1);

feature_index = 0;

for row_index = 1:nz

    for column_index = row_index:nz

        feature_index = ...
            feature_index+1;

        theta(feature_index) = ...
            H(row_index,column_index);

    end

end

H_change_history = ...
    nan(maximum_learning_iterations,1);

L_change_history = ...
    nan(maximum_learning_iterations,1);

K_change_history = ...
    nan(maximum_learning_iterations,1);

bellman_regression_residual_history = ...
    nan(maximum_learning_iterations,1);

spectral_radius_history = ...
    nan(maximum_learning_iterations,1);

game_rcond_history = ...
    nan(maximum_learning_iterations,1);

pursuer_curvature_history = ...
    nan(maximum_learning_iterations,1);

evader_curvature_history = ...
    nan(maximum_learning_iterations,1);

learning_converged = false;

for learning_iteration = 1:maximum_learning_iterations

    H_old = H;
    L_old = L;
    K_old = K;
    theta_old = theta;

    successor_feature_matrix = ...
        zeros(Ndata,number_of_features);

    value_iteration_target = ...
        zeros(Ndata,1);

    for k = 1:Ndata

        x_successor = ...
            x_data(:,k+1);

        uP_successor = ...
            L_old*x_successor;

        uE_successor = ...
            K_old*x_successor;

        z_successor = [ ...
            x_successor;
            uP_successor;
            uE_successor];

        phi_successor = ...
            zeros(1,number_of_features);

        feature_index = 0;

        for row_index = 1:nz

            for column_index = row_index:nz

                feature_index = ...
                    feature_index+1;

                if row_index == column_index

                    phi_successor(feature_index) = ...
                        z_successor(row_index) ...
                        *z_successor(column_index);

                else

                    phi_successor(feature_index) = ...
                        2*z_successor(row_index) ...
                        *z_successor(column_index);

                end

            end

        end

        successor_feature_matrix(k,:) = ...
            phi_successor;

        value_iteration_target(k) = ...
            stage_cost_data(k) ...
            + phi_successor*theta_old;

    end

    theta_scaled_new = ...
        Phi_data_scaled\value_iteration_target;

    theta_new = ...
        theta_scaled_new./data_column_scale';

    if any(~isfinite(theta_new))

        error([ ...
            'The Q-learning value-iteration parameter vector contains ' ...
            'NaN or Inf values.']);

    end

    H_new = ...
        zeros(nz,nz);

    feature_index = 0;

    for row_index = 1:nz

        for column_index = row_index:nz

            feature_index = ...
                feature_index+1;

            H_new(row_index,column_index) = ...
                theta_new(feature_index);

            H_new(column_index,row_index) = ...
                theta_new(feature_index);

        end

    end

    H_new = ...
        0.5*(H_new+H_new');

    if any(~isfinite(H_new(:)))

        error([ ...
            'The learned Q-function matrix contains NaN or Inf values.']);

    end

    pursuer_input_index = ...
        nx+1;

    evader_input_index = ...
        nx+2;

    input_indices = [ ...
        pursuer_input_index ...
        evader_input_index];

    game_matrix_new = ...
        H_new(input_indices,input_indices);

    state_cross_new = ...
        H_new(input_indices,1:nx);

    updated_game_rcond = ...
        rcond(game_matrix_new);

    if ~isfinite(updated_game_rcond) || ...
       updated_game_rcond < 1e-13

        error([ ...
            'The learned value-iteration game matrix is numerically ' ...
            'singular.']);

    end

    HPP_new = ...
        H_new(pursuer_input_index,pursuer_input_index);

    HPE_new = ...
        H_new(pursuer_input_index,evader_input_index);

    HEP_new = ...
        H_new(evader_input_index,pursuer_input_index);

    HEE_new = ...
        H_new(evader_input_index,evader_input_index);

    updated_pursuer_curvature = ...
        HPP_new;

    updated_evader_curvature = ...
        HEE_new ...
        - HEP_new*(HPP_new\HPE_new);

    if updated_pursuer_curvature <= 0

        error([ ...
            'The learned Q-function does not have positive pursuer ' ...
            'curvature.']);

    end

    if updated_evader_curvature >= 0

        error([ ...
            'The learned Q-function does not have negative evader ' ...
            'curvature.']);

    end

    gain_pair_new = ...
        -(game_matrix_new\state_cross_new);

    L_new = ...
        gain_pair_new(1:nuP,:);

    K_new = ...
        gain_pair_new(nuP+1:nuP+nuE,:);

    if any(~isfinite(L_new(:))) || ...
       any(~isfinite(K_new(:)))

        error([ ...
            'The recovered pursuer or evader gain contains ' ...
            'NaN or Inf values.']);

    end

    H_change_history(learning_iteration) = ...
        norm(H_new-H_old,'fro');

    L_change_history(learning_iteration) = ...
        norm(L_new-L_old,'fro');

    K_change_history(learning_iteration) = ...
        norm(K_new-K_old,'fro');

    bellman_regression_residual_history(learning_iteration) = ...
        norm( ...
        Phi_data*theta_new-value_iteration_target, ...
        2) ...
        /(1+norm(value_iteration_target,2));

    Acl_iteration = ...
        Ad+B*L_new+E*K_new;

    spectral_radius_history(learning_iteration) = ...
        max(abs(eig(Acl_iteration)));

    game_rcond_history(learning_iteration) = ...
        updated_game_rcond;

    pursuer_curvature_history(learning_iteration) = ...
        updated_pursuer_curvature;

    evader_curvature_history(learning_iteration) = ...
        updated_evader_curvature;

    H = H_new;
    L = L_new;
    K = K_new;
    theta = theta_new;

    if H_change_history(learning_iteration) < learning_tolerance && ...
       L_change_history(learning_iteration) < learning_tolerance && ...
       K_change_history(learning_iteration) < learning_tolerance

        learning_converged = true;
        break;

    end

end

learning_iteration_count = ...
    learning_iteration;

H_change_history = ...
    H_change_history(1:learning_iteration_count);

L_change_history = ...
    L_change_history(1:learning_iteration_count);

K_change_history = ...
    K_change_history(1:learning_iteration_count);

bellman_regression_residual_history = ...
    bellman_regression_residual_history( ...
    1:learning_iteration_count);

spectral_radius_history = ...
    spectral_radius_history(1:learning_iteration_count);

game_rcond_history = ...
    game_rcond_history(1:learning_iteration_count);

pursuer_curvature_history = ...
    pursuer_curvature_history(1:learning_iteration_count);

evader_curvature_history = ...
    evader_curvature_history(1:learning_iteration_count);

if ~learning_converged

    warning([ ...
        'Q-learning value iteration reached the maximum iteration ' ...
        'count before all convergence criteria were satisfied.']);

end

%% ========================================================================
% 10. FINAL LEARNED-POLICY CHECKS
%% ========================================================================

Acl = ...
    Ad+B*L+E*K;

closed_loop_radius = ...
    max(abs(eig(Acl)));

if ~isfinite(closed_loop_radius)

    error([ ...
        'The learned closed-loop spectral radius is not finite.']);

end

if closed_loop_radius >= 1

    error([ ...
        'The learned Q-learning value-iteration gain pair does not ' ...
        'produce a Schur-stable closed loop.']);

end

pursuer_input_index = ...
    nx+1;

evader_input_index = ...
    nx+2;

input_indices = [ ...
    pursuer_input_index ...
    evader_input_index];

game_matrix = ...
    H(input_indices,input_indices);

state_cross = ...
    H(input_indices,1:nx);

game_matrix_rcond = ...
    rcond(game_matrix);

if ~isfinite(game_matrix_rcond) || ...
   game_matrix_rcond < 1e-13

    error([ ...
        'The final learned game matrix is numerically singular.']);

end

HPP = ...
    H(pursuer_input_index,pursuer_input_index);

HPE = ...
    H(pursuer_input_index,evader_input_index);

HEP = ...
    H(evader_input_index,pursuer_input_index);

HEE = ...
    H(evader_input_index,evader_input_index);

pursuer_curvature_minimum = ...
    HPP;

evader_curvature_maximum = ...
    HEE-HEP*(HPP\HPE);

if pursuer_curvature_minimum <= 0

    error([ ...
        'The final learned pursuer direction is not strictly convex.']);

end

if evader_curvature_maximum >= 0

    error([ ...
        'The final learned evader direction is not strictly concave.']);

end

H_symmetry_error = ...
    norm(H-H','fro');

policy_embedding = [ ...
    eye(nx);
    L;
    K];

P_learned = ...
    policy_embedding'*H*policy_embedding;

P_learned = ...
    0.5*(P_learned+P_learned');

minimum_P_learned_eigenvalue = ...
    min(real(eig(P_learned)));

%% Fixed-point Bellman consistency

successor_feature_matrix_final = ...
    zeros(Ndata,number_of_features);

for k = 1:Ndata

    x_successor = ...
        x_data(:,k+1);

    z_successor = [ ...
        x_successor;
        L*x_successor;
        K*x_successor];

    phi_successor = ...
        zeros(1,number_of_features);

    feature_index = 0;

    for row_index = 1:nz

        for column_index = row_index:nz

            feature_index = ...
                feature_index+1;

            if row_index == column_index

                phi_successor(feature_index) = ...
                    z_successor(row_index) ...
                    *z_successor(column_index);

            else

                phi_successor(feature_index) = ...
                    2*z_successor(row_index) ...
                    *z_successor(column_index);

            end

        end

    end

    successor_feature_matrix_final(k,:) = ...
        phi_successor;

end

fixed_point_target = ...
    stage_cost_data ...
    + successor_feature_matrix_final*theta;

fixed_point_bellman_residual = ...
    norm( ...
    Phi_data*theta-fixed_point_target, ...
    2) ...
    /(1+norm(fixed_point_target,2));

final_regression_residual = ...
    bellman_regression_residual_history(end);

%% ========================================================================
% 11. OFFLINE MODEL-BASED REFERENCE FOR VALIDATION ONLY
%% ========================================================================

reference_tolerance = 1e-13;
maximum_reference_iterations = 5000;

P_reference = ...
    eye(nx);

for reference_iteration = 1:maximum_reference_iterations

    P_reference_old = ...
        P_reference;

    HPP_reference_old = ...
        RP+B'*P_reference_old*B;

    HPE_reference_old = ...
        B'*P_reference_old*E;

    HEP_reference_old = ...
        E'*P_reference_old*B;

    HEE_reference_old = ...
        E'*P_reference_old*E-RE;

    reference_game_matrix_old = [ ...
        HPP_reference_old HPE_reference_old;
        HEP_reference_old HEE_reference_old];

    reference_state_cross_old = [ ...
        B'*P_reference_old*Ad;
        E'*P_reference_old*Ad];

    P_reference_new = ...
        Q+Ad'*P_reference_old*Ad ...
        - [ ...
        Ad'*P_reference_old*B ...
        Ad'*P_reference_old*E] ...
        *(reference_game_matrix_old ...
        \reference_state_cross_old);

    P_reference_new = ...
        0.5*(P_reference_new+P_reference_new');

    P_reference = ...
        P_reference_new;

    if norm( ...
       P_reference_new-P_reference_old, ...
       'fro') < reference_tolerance

        break;

    end

end

H_reference = [ ...
    Q+Ad'*P_reference*Ad, ...
    Ad'*P_reference*B, ...
    Ad'*P_reference*E;
    B'*P_reference*Ad, ...
    RP+B'*P_reference*B, ...
    B'*P_reference*E;
    E'*P_reference*Ad, ...
    E'*P_reference*B, ...
    E'*P_reference*E-RE];

H_reference = ...
    0.5*(H_reference+H_reference');

reference_game_matrix = ...
    H_reference(input_indices,input_indices);

reference_state_cross = ...
    H_reference(input_indices,1:nx);

reference_gain_pair = ...
    -(reference_game_matrix\reference_state_cross);

L_reference = ...
    reference_gain_pair(1:nuP,:);

K_reference = ...
    reference_gain_pair(nuP+1:nuP+nuE,:);

Acl_reference = ...
    Ad+B*L_reference+E*K_reference;

reference_closed_loop_radius = ...
    max(abs(eig(Acl_reference)));

H_reference_error = ...
    norm(H-H_reference,'fro');

H_reference_relative_error = ...
    H_reference_error ...
    /max(norm(H_reference,'fro'),eps);

L_reference_error = ...
    norm(L-L_reference,'fro');

L_reference_relative_error = ...
    L_reference_error ...
    /max(norm(L_reference,'fro'),eps);

K_reference_error = ...
    norm(K-K_reference,'fro');

K_reference_relative_error = ...
    K_reference_error ...
    /max(norm(K_reference,'fro'),eps);

P_reference_error = ...
    norm(P_learned-P_reference,'fro');

P_reference_relative_error = ...
    P_reference_error ...
    /max(norm(P_reference,'fro'),eps);

closed_loop_radius_error = ...
    abs(closed_loop_radius-reference_closed_loop_radius);

