%% ========================================================================
% Ch 5 Extended_QLearning_PI_Code.m
%
% Disturbance-aware extension of the supplied off-policy quadratic
% Q-learning value-iteration program.
%
% The nominal model-free value-iteration structure is retained:
%   1. Construct one fixed feature matrix from measured transitions.
%   2. Form a bootstrapped target using the current successor Q-value.
%   3. Solve the fixed-feature least-squares problem.
%   4. Reconstruct the symmetric Q-function matrix.
%   5. Recover the pursuer and evader saddle gains.
%   6. Repeat until the Q-function matrix and both gains converge.
%
% The disturbance-aware extension adds:
%   1. Direct command-versus-realized-input offset calibration.
%   2. Disturbance-consistent off-policy data construction.
%   3. Affine command compensation.
%   4. Nominal, uncompensated, estimated-compensation, and
%      exact-compensation execution cases.
%   5. Monte Carlo estimator validation.
%   6. Feature-rank, conditioning, Bellman, saddle, stability, reference,
%      trajectory, cost, and command-bookkeeping diagnostics.
%   7. CSV, MAT, text-report, FIG, and 300-DPI PNG exports.
%
% The plant matrices are used by the MATLAB simulation environment and
% offline validation calculations. They are not used in the Q-learning
% Bellman regression or learned-policy recovery equations.
%% ========================================================================

