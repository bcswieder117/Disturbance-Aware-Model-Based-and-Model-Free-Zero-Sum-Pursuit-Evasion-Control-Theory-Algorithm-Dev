          *RE*uE_actual_nominal(k);

    %% Persistent offsets without compensation

    uP_policy_uncompensated(k) = ...
        L*x_uncompensated(:,k);

    uE_policy_uncompensated(k) = ...
        K*x_uncompensated(:,k);

    uP_command_uncompensated(k) = ...
        uP_policy_uncompensated(k);

    uE_command_uncompensated(k) = ...
