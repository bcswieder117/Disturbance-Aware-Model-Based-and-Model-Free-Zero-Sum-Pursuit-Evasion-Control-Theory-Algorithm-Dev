        x_estimated_compensation(:,k)' ...
        *Q*x_estimated_compensation(:,k) ...
        + uP_actual_estimated(k)' ...
          *RP*uP_actual_estimated(k) ...
        - uE_actual_estimated(k)' ...
          *RE*uE_actual_estimated(k);

    %% Exact compensation

    uP_policy_exact(k) = ...
        L*x_exact_compensation(:,k);

    uE_policy_exact(k) = ...
        K*x_exact_compensation(:,k);

    uP_command_exact(k) = ...
        uP_policy_exact(k)-bP_true;

    uE_command_exact(k) = ...
        uE_policy_exact(k)-bE_true;

    uP_actual_exact(k) = ...
        uP_command_exact(k)+bP_true;

    uE_actual_exact(k) = ...
        uE_command_exact(k)+bE_true;

